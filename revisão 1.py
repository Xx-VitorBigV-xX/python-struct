#adicao
print(3 + 2)

#subtracao
print(7-5)

#multiplicacao
print(2 * 3)
print(3 * 3.5)
print(4 * 5.0)

#divisao
print(10/3)
print(9/3.0)


#divisao calculando o coeficiente
print(5//2)
print(5.0//2)
print(4//1.2)

#divisao calculando o resto

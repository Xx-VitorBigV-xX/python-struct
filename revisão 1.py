#potenciacao: exponenciacao
print(3**2)
print(2 ** 10)

#potenciacao radiciacao
print(9 ** (float(1)/2))
#elevando em decimal ao inves de fracao
print(16 ** 0.5)

#raiz cubica
print(27 ** (float(1)/3))


# excercicios
# 2+3*4/5-6
